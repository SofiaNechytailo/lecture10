public class Student {
     String name;
     String surname;
     int age;
    public void setName (String _name){
        name = _name;
    }
    public String getName () {
        return name;
    }
    public void setSurname (String _surname){
        surname = _surname;
    }
    public String getSurname () {
        return surname;
    }
    public void setAge (int _age){
        age = _age;
    }
    public int getAge () {
        return age;
    }

}
